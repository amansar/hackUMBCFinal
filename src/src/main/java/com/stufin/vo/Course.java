package com.stufin.vo;

public class Course {

	private String name;
	
	private String number;
	
	private String desc;
	
	private String venue;
	
	private String numOfCrdt;
	
	private String costPrCrdt;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getVenue() {
		return venue;
	}

	public void setVenue(String venue) {
		this.venue = venue;
	}

	public String getNumOfCrdt() {
		return numOfCrdt;
	}

	public void setNumOfCrdt(String numOfCrdt) {
		this.numOfCrdt = numOfCrdt;
	}

	public String getCostPrCrdt() {
		return costPrCrdt;
	}

	public void setCostPrCrdt(String costPrCrdt) {
		this.costPrCrdt = costPrCrdt;
	}
	
	
}
